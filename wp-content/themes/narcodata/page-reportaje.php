				<p>Ante una enorme oferta de blogs o cuentas de redes sociales que difunden información sin filtro periodístico, NarcoData quiere ser lo contrario. Creamos una herramienta que recopila información corroborada y presentada de forma ordenada y sencilla, con el rigor y la innovación que caracteriza al periodismo de Animal Político.</p>

				<p>NarcoData plasma la historia reciente del crimen organizado en visualizaciones interactivas que cuentan el surgimiento de los cárteles, sus líderes, los conflictos entre grupos, su expansión geográfica y sus actividades delictivas.</p>

				<p>La idea de este sitio surge cuando Animal Político obtiene, gracias la ley de transparencia, un documento de la Procuraduría General de la República que muestra todas las células delictivas que operan en México y el cártel al que obedecen. Ese documento, además, tumba varios “mitos” creados por funcionarios públicos, como que el Distrito Federal está libre de cárteles o que las organizaciones criminales pierden poder con el actual gobierno federal.</p>

				<p>Al analizar ese documento, periodistas, diseñadores y programadores de Animal Político nos planteamos el reto de presentar una plataforma digital que explicara cómo el narcotráfico llegó a su tamaño e influencia actual.</p>

				<p>Para lograrlo sumamos a la compañía chilena de medios <a href="#">Poderopedia</a>, que explica en sitios web las relaciones entre los empresarios más prominentes y su influencia en la política de Chile, Colombia y Venezuela.</p>

				<p>La experiencia de Poderopedia nos permitió mirar a los cárteles, hasta cierto punto, como poderosas organizaciones comerciales con influencia en los asuntos públicos del país. Por ello, para explicar la evolución del crimen organizado, parte de los elementos tecnológicos de Poderopedia se adaptaron a NarcoData.</p>

				<p>A través de solicitudes de información, entrevistas, revisiones hemerográficas y bibliográficas, y con la asesoría de expertos como Alejandro Hope (editor de Seguridad de El Daily Post con más de xx años como analista) logramos darle solidez a la información. A ello le sumamos elementos de diseño y programación que permiten al lector tener en sus dispositivos electrónicos una herramienta en actualización constante para analizar la historia del narcotráfico en México.</p>

				<p>Este proyecto es una realidad gracias al financiamiento de la aceleradora de periodismo de datos HacksLabs y de Hivos, organización internacional que aporta fondos para proyectos de países en desarrollo.</p>

