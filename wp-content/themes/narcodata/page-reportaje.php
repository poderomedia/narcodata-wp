				<p>Cientos de miles de muertos. Nueve cárteles activos. Estados enteros amenazados por la violencia. Capos de la droga que se vuelven leyenda... El narcotráfico en México tiene proporciones descomunales y es un entramado difícil de entender.</p>

				<p>Animal Político detectó esa necesidad informativa y creó NarcoData, un sitio interactivo que presenta una radiografía de las últimas cuatro décadas del crimen organizado, para que los lectores se expliquen, de manera sencilla, la evolución y el avance de los grupos delincuenciales en el país.</p>

